    public void tabelaQuarto(){
        DefaultTableModel tabela2 = (DefaultTableModel) qtTabela.getModel();
        tabela2.setNumRows(0);
        QuartoDAO qtDao = new QuartoDAO();
        
        for(Quarto c : qtDao.read()){
        
            tabela2.addRow(new Object[]{
                c.getNumero(),
                c.getAndar(),
                c.getPreco(),
                c.getEstado(),
                c.getPeriodo()
            });
            
        }
    }
    
    public void tabelaCliente(){
        DefaultTableModel tabela1 = (DefaultTableModel) tabelaMenu1.getModel();
        tabela1.setNumRows(0);
        ClienteDAO clDao = new ClienteDAO();
        
        for(Cliente c : clDao.read()){
        
            tabela1.addRow(new Object[]{
                
                c.getId_quarto(),
                c.getSituacao(),
                c.getNome()  
            });
            
        }
    
    }
    
