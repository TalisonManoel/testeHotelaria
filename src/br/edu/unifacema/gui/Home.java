    private void btCheckinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCheckinActionPerformed
        
        Quarto cb = (Quarto) cbQuartos.getSelectedItem();
        Cliente cl = new Cliente();
        int id = cb.getId_quarto(); 
        
        if((barraPesquisa.getText().equals("") | cbQuartos.getSelectedIndex()== -1) ){
            JOptionPane.showMessageDialog(null,"Os campos devem ser preenchidos!");
        } else {
           
           cl.setNome(barraPesquisa.getText());
           cl.setCpf(clCpf.getText());
           cl.setId_quarto(id);
           cl.setSituacao("quite");
           cldao.create(cl);
           
           Quarto qt = qtdao.readForId(id);
           qt.setEstado("ocupado");
           qtdao.update(qt);
           
           cbQuartos.removeAllItems();
           tabelaQuarto();
           tabelaCliente();
           combBoxP1();
        }
    }//GEN-LAST:event_btCheckinActionPerformed

